type literal =
  | String of string [@printer fun fmt s -> fprintf fmt "\"%s\"" s]
  | Char of char [@printer fun fmt c -> fprintf fmt "\'%c\'" c]
  | Float of string [@printer fun fmt f -> fprintf fmt "%s" f]
  | Int of string [@printer fun fmt i -> fprintf fmt "%s" i]
[@@deriving show]

type comment =
  | One [@printer fun fmt _ -> fprintf fmt "One"]
  | Multi [@printer fun fmt _ -> fprintf fmt "Multi"]
  | Doc of string [@printer fun fmt s -> fprintf fmt "Doc: %s" s]
[@@deriving show]

type token =
  | Separator of separator
      [@printer fun fmt s -> fprintf fmt "%s" (show_separator s)]
  | Operator of operator
      [@printer fun fmt o -> fprintf fmt "%s" (show_operator o)]
  | Keyword of keyword
      [@printer fun fmt k -> fprintf fmt "%s" (show_keyword k)]
  | Identifier of string
      [@printer fun fmt s -> fprintf fmt "%s" s]
  | Literal of literal
      [@printer fun fmt l -> fprintf fmt "%s" (show_literal l)]
  | Comment of comment
      [@printer fun fmt c -> fprintf fmt "%s" (show_comment c)]
[@@deriving show]
