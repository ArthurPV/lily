  | LeftShift [@printer fun fmt _ -> fprintf fmt "<"]
  | RightShift [@printer fun fmt _ -> fprintf fmt ">"]
  | LeftShiftEq [@printer fun fmt _ -> fprintf fmt "<="]
  | RightShiftEq [@printer fun fmt _ -> fprintf fmt ">="]
  | BangEq [@printer fun fmt _ -> fprintf fmt "!="]
  | Interrogation [@printer fun fmt _ -> fprintf fmt "?"]
  | Ampersand [@printer fun fmt _ -> fprintf fmt "&"]
[@@deriving show]

