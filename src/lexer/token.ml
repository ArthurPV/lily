type separator =
  | Dot [@printer fun fmt _ -> fprintf fmt "."]
  | DotDotDot [@printer fun fmt _ -> fprintf fmt "..."]
  | Comma [@printer fun fmt _ -> fprintf fmt ","]
  | Colon [@printer fun fmt _ -> fprintf fmt ":"]
  | ColonColon [@printer fun fmt _ -> fprintf fmt "::"]
  | Bar [@printer fun fmt _ -> fprintf fmt "|"]
  | Arrow [@printer fun fmt _ -> fprintf fmt "->"]
  | InverseArrow [@printer fun fmt _ -> fprintf fmt "<-"]
  | FatArrow [@printer fun fmt _ -> fprintf fmt "=>"]
  | At [@printer fun fmt _ -> fprintf fmt "@"]
  | LeftParen [@printer fun fmt _ -> fprintf fmt "("]
  | RightParen [@printer fun fmt _ -> fprintf fmt ")"]
  | LeftBrace [@printer fun fmt _ -> fprintf fmt "{"]
  | RightBrace [@printer fun fmt _ -> fprintf fmt "}"]
  | LeftHook [@printer fun fmt _ -> fprintf fmt "["]
  | RightHook [@printer fun fmt _ -> fprintf fmt "]"]
  | Hashtag [@printer fun fmt _ -> fprintf fmt "#"]
  | Wave [@printer fun fmt _ -> fprintf fmt "~"]
  | Eof [@printer fun fmt _ -> fprintf fmt "EOF"]
[@@deriving show]

