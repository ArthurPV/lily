  | PlusEq [@printer fun fmt _ -> fprintf fmt "+="]
  | MinusEq [@printer fun fmt _ -> fprintf fmt "-="]
  | StarEq [@printer fun fmt _ -> fprintf fmt "*="]
  | SlashEq [@printer fun fmt _ -> fprintf fmt "/="]
  | PercentageEq [@printer fun fmt _ -> fprintf fmt "%%="]
  | HatEq [@printer fun fmt _ -> fprintf fmt "^="]
  | Eq [@printer fun fmt _ -> fprintf fmt "="]
  | EqEq [@printer fun fmt _ -> fprintf fmt "=="]
